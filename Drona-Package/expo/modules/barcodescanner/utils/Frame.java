package expo.modules.barcodescanner.utils;

public class Frame
{
  private ImageDimensions mDimensions;
  private com.google.android.gms.vision.Frame mFrame;
  
  public Frame(com.google.android.gms.vision.Frame paramFrame, ImageDimensions paramImageDimensions)
  {
    mFrame = paramFrame;
    mDimensions = paramImageDimensions;
  }
  
  public ImageDimensions getDimensions()
  {
    return mDimensions;
  }
  
  public com.google.android.gms.vision.Frame getFrame()
  {
    return mFrame;
  }
}
